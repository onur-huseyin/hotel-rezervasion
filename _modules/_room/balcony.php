                            <p>
                                Der Zugang zur großzügig geschnittenen ca. 95 qm großen Wohnung erfolgt über eine Außentreppe und den nach Süden ausgerichteten vorderen Balkon. Sie betreten die Ferienwohnung durch einen kleinen Flur mit Garderobe und finden rechter Hand die gemütliche Wohnküche und linker Hand das komfortabel eingerichtete Wohnzimmer. Gehen Sie ein paar Meter weiter, so befindet sich wiederum zur linken Seite ein schön und modern ausgestattetes Wellnessbad. Diesem gegenüber befindet sich der Saunaraum. An Bad und Saunaraum grenzen zwei spiegelgleiche Schlafzimmer, die wiederum ihrerseits zum zweiten, gartenseitig gelegenen Balkon führen. Beide Balkone sind übrigens jeweils ca. 20 qm groß und mit hochwertigem Bangkirai Holz belegt. In der Sommerzeit erlaubt die stilvolle Teaksitzgruppe gemütliche Stunden im Freien zu verbringen.
                            </p>
                            <p>
                                Die Küche ist voll ausgestattet und verfügt über eine Einbauküche mit Kirschholzverkleidung (Kühlschrank mit TKF, Geschirrspülmaschine, Herd m. Cerankochfeld und Umlufthaube). In der großzügigen Küche steht ein stilvoller Esstisch, an dem bis zu 6 Personen komfortabel Platz finden. Selbstverständlich finden Sie alles Geschirr, Gläser, Küchenutensilien usw., die auch einen längeren Aufenthalt komfortabel gestalten lassen zu Ihrer Verfügung.
                            </p>
                            <p>
                                Das im Jahre 2016 komplett neu gestaltete Badezimmer ist geschmackvoll gefliest und verfügt sowohl über eine auf Empore gestellte Badewanne, als auch über eine separate Dusche. Neben der eingebauten Waschmaschine sorgen moderne Badezimmermöbel und ein beheizter Handtuchhalter für zusätzlichen Komfort im Bad. Im gegenüber gelegenen Saunaraum bieten eine luxuriöse Sauna bis zu 4 Personen gleichzeitig sowie eine schöne Holzbank dem Gast Entspannungsmöglichkeiten. Die im Dezember 2017 eingebaute, sehr hochwertige Sauna ist über einen Münzautomaten (2,00 € / Std.) leicht zu bedienen.
                            </p>
                            <p>
                                Die Anmutung der Ferienwohnung lässt sich am besten als moderner Kolonialstil beschreiben; Kirschholzlaminat in Verbindung mit tropischen Hölzern, direkte und indirekte Beleuchtung sowie wertig ausgesuchtes Mobiliar garantieren Ihnen erholsame Tage in schönster Umgebung.
