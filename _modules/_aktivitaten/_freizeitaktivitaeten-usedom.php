                    </p>

                    <p>
                    Für Abwechslung und Unterhaltung der Inselbesucher sorgen das ganze Jahr hindurch zahlreiche Veranstaltungen. Eine von ihnen ist der ‚Grand Schlemm‘ jeweils am Wochenende nach dem Feiertag Christi Himmelfahrt. Treffpunkt für dieses kulinarische Ereignis ist die Seebrücke im Heringsdorfer Ortsteil Ahlbeck. Auf dem gesamten Weg von Ahlbeck zum Nachbarort Bansin entlang dem Ostseestrand bieten ein Dutzend Spitzenköche der führenden Restaurants auf Usedom ihre Speisen und Spezialitäten an einem eigenen ‚Rastplatz de Luxe‘ an. Komplettiert wird das Angebot mit Weinen aus den bekannten deutschen Anbaugebieten. Das alljährliche Grand Schlemm gilt als ‚Fine Dining am Ostseestrand auf Usedom‘.
                    </p>

