                            Nach der Ostseeinsel Rügen ist Usedom die deutschlandweit zweitgrößte Insel. Zu den bekannten Ausflugszielen gehören auf Usedom das Seebad Zinnowitz im Inselwesten, die Kaiserbäder Ahlbeck, Heringsdorf und Bansin entgegengesetzt im Osten, sowie die Gemeinde Wolgast im Norden der Insel. Peenemünde ist der älteste Ort, und Karlshagen ist ein beliebtes Ziel für den ausgiebigen Strandurlaub.

